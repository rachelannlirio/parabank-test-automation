function getBaseUrl(env: string | undefined): string {
  switch (env) {
    case 'qa':
      return 'https://qa-parabank.parasoft.com'
    case 'staging':
      return 'https://staging-parabank.parasoft.com'
    case 'prod':
    default:
      return 'https://parabank.parasoft.com'
  }
}

